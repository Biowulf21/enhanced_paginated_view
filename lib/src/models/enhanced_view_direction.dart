/// Enum representing the direction of an enhanced view.
///
/// The [EnhancedViewDirection] enum has two possible values:
/// - `forward`: Represents the forward direction.
/// - `reverse`: Represents the reverse direction.
///
/// This enum is used in the `enhanced_paginated_view` package to indicate the direction of an enhanced view.
/// It is typically used in conjunction with other classes and methods in the package to control the behavior of the view.
///
/// Example usage:
/// ```dart
/// EnhancedViewDirection direction = EnhancedViewDirection.forward;
/// ```
enum EnhancedViewDirection {
  /// Represents the forward direction.
  forward,

  /// Represents the reverse direction.
  reverse;
}
